int main() {
	string txt = "ABCS";
//	cin>>txt;
	int tl = txt.length();
	char mp[tl*tl*2][tl*tl*2];
	memset(mp, '.', sizeof(mp));
	int shx=0;
	int shy=0;
	
	for(int h=0;h<tl;h++) {
		for(int k=0;k<tl-h;k++) {
			for(int i=0;i<tl*tl;i++) {
				for(int j=0;j<tl*tl*2;j++) {
					if(abs(i-tl)+abs(j-tl)<=tl&&j>=tl+1) {
						mp[(i)+shy-1+(tl)*h][((j)-tl-1+(tl*h))*2]=txt[abs(j-i)%tl];
				
					}
				}
			}
			shy+=2*tl;
		}
		shy=0;
	}
	
	
	
	for(int i=0;i<tl*tl*2;i++) {
		for(int j=0;j<tl*tl*2-1;j++) {
			cout<<mp[i][j];
		}
		cout<<"\n";
	}
	
		
	return 0;
}

